# Напишите программу, которая запрашивает у пользователя число n, а затем выводит n первых строк
# треугольника Паскаля. Обеспечьте отказоустойчивость при введении пользователем не валидного значения n (т.е. не целого положительного числа)


try:
    n = int(input("Введите количество строк треугольника Паскаля: "))
    if n < 0:
        print("Число должно быть положительным!")
except:
    print("Введите целое число!")

# Создаем треугольник Паскаля
triangle = []
for i in range(n):
    row = [1] * (i + 1)
    for j in range(1, i):
        row[j] = triangle[i-1][j-1] + triangle[i-1][j]
    triangle.append(row)

print(f"\nПервые {n} строк треугольника Паскаля:")
for row in triangle:
    print(' '.join(map(str, row)))