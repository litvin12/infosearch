<!-- В скрипте z3-5.php

Создайте массив $treug[] "треугольных" чисел, т.е. чисел вида n(n+1)/2 (где n=1,2,: 10) и выведите значения этого массива на экран в строку (через 2 пробела).

Создайте массив $kvd[] квадратов натуральных чисел от 1 до 10, выведите значения этого массива на экран в строку.

Объедините эти 2 массива в массив $rez[], выведите результат на экран.

Отсортируйте массив $rez[], выведите результат на экран.

Удалите в массиве $rez[] первый элемент, выведите результат на экран.

С помощью функции array_unique() удалите из массива $rez[] повторяющиеся элементы, результат занесите в массив $rez1[] и выведите его на экран.

(Использовать листинги 9-2 - 9-10). -->

<html>
<head>
    <title>Работа с массивами</title>
</head>
<body>
<?php
// 1. Треугольные числа
$treug = [];
for ($n = 1; $n <= 10; $n++) {
    $treug[] = $n * ($n + 1) / 2;
}
echo "Треугольные числа:<br>";
foreach ($treug as $val) {
    echo $val . "  "; // два пробела
}
echo "<br><br>";

// 2. Квадраты
$kvd = [];
for ($n = 1; $n <= 10; $n++) {
    $kvd[] = $n * $n;
}
echo "Квадраты чисел:<br>";
foreach ($kvd as $val) {
    echo $val . "  ";
}
echo "<br><br>";

// 3. Объединение
$rez = array_merge($treug, $kvd);
echo "Объединённый массив:<br>";
foreach ($rez as $val) {
    echo $val . " ";
}
echo "<br><br>";

// 4. Сортировка
sort($rez);
echo "Отсортированный массив:<br>";
foreach ($rez as $val) {
    echo $val . " ";
}
echo "<br><br>";

// 5. Удаление первого элемента
array_shift($rez);
echo "После удаления первого элемента:<br>";
foreach ($rez as $val) {
    echo $val . " ";
}
echo "<br><br>";

// 6. Удаление дублей
$rez1 = array_unique($rez);
echo "После удаления повторов (rez1):<br>";
foreach ($rez1 as $val) {
    echo $val . " ";
}
?>
