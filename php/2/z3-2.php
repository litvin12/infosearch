<!-- Используя вложенные циклы for в скрипте z3-2.php отобразите на экране таблицу сложения чисел от 1 до 10.
 При этом цвет цифр в верхней строке и левом столбце должен быть задан через $color вне циклов, а в левой верхней ячейке
 должен стоять знак "+" красного цвета. Ширина рамки таблицы равна 1, отступ содержимого ячеек от границы равен 5.

+	2	3	...	10
2	4	5	...	12
3	5	6	...	13
...	...	...	...	...
10	12	13	...	20

(Использовать листинг 7-6). -->

<html>
<head>
    <title>Таблица сложения 10×10</title>
</head>
<body>
<?php
$color = "blue";   // цвет для заголовков строки и столбца
$max   = 10;
?>
<table border="1" cellpadding="5">
<?php
for ($y = 0; $y <= $max; $y++) {
    echo "<tr>\n";
    for ($x = 0; $x <= $max; $x++) {
        // Левая верхняя ячейка
        if ($x == 0 && $y == 0) {
            echo "\t<td style=\"color:red;\">+</td>\n";
        }
        // Заголовок верхней строки
        elseif ($y == 0) {
            echo "\t<td style=\"color:$color;\">$x</td>\n";
        }
        // Заголовок левого столбца
        elseif ($x == 0) {
            echo "\t<td style=\"color:$color;\">$y</td>\n";
        }
        // Остальные ячейки = сумма
        else {
            $sum = $x + $y;
            echo "\t<td>$sum</td>\n";
        }
    }
    echo "</tr>\n";
}
?>
</table>
</body>
</html>